            <h4>Описание</h4>
            <p>Модель DS-2DF8336IV-AEL является наиболее функциональным устройством в классе SpeedDome компании HikVision. Вандалостойкий (IK10) корпус IP-камеры защищен от погодных воздействий по стандарту IP66, имеется встроенный нагреватель и потому рабочие температуры получили широкий диапазон от -60° до +65°С. Совокупность этих характеристик позволяет использовать прибор не просто на сильном морозе, но и в экстремальных условиях Арктики. Разрешение матрицы 3Мп и 36-кратный зум дают возможность получать бескомпромиссную детализацию любой точки наблюдаемой сцены. Модель снабжена аппаратной видеоаналитикой, полностью поддерживаемой в ПО TRASSIR: распознавание лиц, обнаружение вторжения, пересечение виртуальной линии, вход / выход из выделенной зоны, оставленный багаж / вещи, исчезновение предметов, расфокусировка, изменение сцены и других параметров, аудиодетекция. Имеется авто-трекинг, отслеживающий перемещения. Также на «борту» присутствуют средства улучшения изображения WDR 120 дБ (широкий динамический диапазон) / 3D DNR (трехмерное цифровое шумоподавление) / BLC/HLC (компенсация фоновой засветки / инверсия пиков белого) / «Антитуман» (регулировка контраста и компенсация белого «задымления»). Симбиоз перечисленных функций способствует устранению дефектов, вызванных разноконтрастным освещением, шумами в кадре и воздействием на картинку пересвеченных и низкоконтрастных деталей. ROI помогает сфокусироваться только на выбранной наиболее важной области, а за счет понижения качества фиксации остальной части сцены, сократить битрейт. IP-камера поддерживает карты памяти стандарта microSD объемом до 128 ГБ и технологию High-PoE, поэтому для трансляции данных и обеспечения питания к IP-камере достаточно подвести один кабель Ethernet. Встроенная ИК-подсветка дает возможность осуществлять видеофиксацию при слабой освещенности и полной темноте, покрывая гигантскую площадь более 12 га! HikVision DS-2DF8336IV-AEL – идеальный выбор для наивысшего уровня охраны стратегически важных объектов, обеспечения безопасности на территориях с большим скоплением людей или транспорта, например, городских площадях, магистралях, АТП, спортивных и концертных площадках, – там, где требуется панорамный обзор и максимальная детализация изображения.</p>
            <h4>Характеристики</h4>
            <p>В основу HikVision DS-2DF8336IV-AEL положен 1/3" CMOS-сенсор с максимальным разрешением 2048x1536 пикселей, чувствительностью 0.05 Люкс в цвете при F1.6 / 0 Люкс с включенной ИК-подсветкой и скоростью трансляции основного видеопотока 25 Fps. Устройство поддерживает режим работы «день/ночь» и оборудовано механическим ИК-фильтром для коррекции цветовой передачи в светлое время суток и повышения уровня чувствительности в темное. ИК-подсветка с дальностью действия до 200 м дает возможность осуществления ночного наблюдения на площади более 12 тыс. кв. метров, необорудованной дополнительными источниками света или в тех ситуациях, если свет неожиданно погас. Полное отсутствие внешних световых приборов позволяет сэкономить на электричестве в темное время суток десятки тысяч рублей. В дополнение ко всему использование инфракрасных диодов снижает шум, сокращает количество ложных срабатываний детекторов, тем самым уменьшает битрейт и экономит драгоценные мегабайты архива. Поддерживается MultiStream (три видеопотока), предназначенный для снижения нагрузки на видеорегистратор / сеть и упрощения доступа через интернет. Компрессия отснятых материалов осуществляется видеокодеком H.264, также возможно сжатие стандартом MJPEG. Максимальная скорость панорамирования составляет 240°/с, наклона – 200°/с, имеются 8 зон патрулирования по 32 предустановки каждая и встроенный механизм защиты от вибраций. Также в камеру заложена функция «стоп-кадр при повороте», суть которой заключается в «замораживании» изображения на момент поворота объектива, в результате место в архиве не расходуется понапрасну, так как не записываются не несущие полезной информации события. Имеются зоны приватности, позволяющие скрывать отдельные фрагменты сцены, не подлежащие по каким-либо причинам видеоконтролю.</p>
